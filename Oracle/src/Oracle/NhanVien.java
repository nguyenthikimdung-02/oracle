    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Tìm tên nhân viên thông qua mã nhân viên
        try{
            Class.forName("oracle.jdbc.driver.OracleDriver");
            // Tạo kết nối đến server
            Connection con = DriverManager.getConnection(dbURL, username, pass);
            CallableStatement cstmt = con.prepareCall("CALL FindNameNV(?) INTO ?");
            cstmt.registerOutParameter(2,Types.VARCHAR);
            cstmt.setString(1, jTextField4.getText().trim());
            cstmt.executeQuery();
            String tmp = cstmt.getString(2);
            cstmt.close();
            if(tmp==null)
                tmp="Không tìm thấy mã nhân viên tương tự";
            JOptionPane.showMessageDialog(this, tmp);
            con.close();
        }
        catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

