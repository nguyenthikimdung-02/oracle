    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // load nhan vien
        try{
            tblModel = new DefaultTableModel(header,0);
            Class.forName("oracle.jdbc.driver.OracleDriver");
            // Tạo kết nối đến server
            Connection con = DriverManager.getConnection(dbURL, username, pass);
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("select * from USER1.NHANVIEN" );
            
            Vector data = null;
            tblModel.setRowCount(0); 
            if (rs.isBeforeFirst() == false) {
                JOptionPane.showMessageDialog(this, "Chưa có thông tin!");
                return;
            }
 
            while (rs.next()) {
            data = new Vector();
            // Dữ liệu cột 1
            data.add(rs.getString("MANV"));
            data.add(rs.getString("TENID"));
            data.add(rs.getString("TENNV"));
            data.add(rs.getString("NGAYSINH"));
            data.add(rs.getString("GIOITINH"));
            data.add(rs.getString("DIACHI"));
            data.add(rs.getString("LUONG"));
            data.add(rs.getString("NGAYVAOLAM"));
            // Thêm một dòng vào table model
            tblModel.addRow(data);
        }
        // Hiển thị dữ liệu lên table
            jTable1.setModel(tblModel);
            
            con.close();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // thêm nhân viên
        try{
            Class.forName("oracle.jdbc.driver.OracleDriver");
            // Tạo kết nối đến server
            Connection con = DriverManager.getConnection(dbURL, username, pass);
            
            LocalDate localDate = jDateChooser1.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yy");
            String ngaysinh = localDate.format(formatter);
            
            CallableStatement cstmt = con.prepareCall("CALL INSERT_NV(?,?,?,?,?)");
            cstmt.setString(1, jTextField1.getText());
            cstmt.setString(2, jTextField2.getText());
            cstmt.setString(3, ngaysinh);
            cstmt.setString(4, gioitinh);
            cstmt.setString(5, jTextField3.getText());
            cstmt.execute();
            cstmt.close();
            if(cstmt != null)
                JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công.");
            con.close();
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Thêm nhân viên không thành công.");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

